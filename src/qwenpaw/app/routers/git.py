@router.get("/status", response_model=GitStatus, summary="Git status")
async def git_status(request: Request) -> GitStatus:
    """Return branch, ahead/behind counts and changed files.

    Auto-initialises a git repo (with an initial commit) the first time the
    workspace is opened if it does not already have one.
    """
    workspace = await get_agent_for_request(request)
    cwd = get_coding_dir(workspace)

    branch, needs_init = await _resolve_branch(cwd)
    if needs_init:
        branch = await _auto_init_repo(cwd)
        if not branch:
            return GitStatus(branch="", changes=[], ahead=0, behind=0)

    # Ahead / behind (may fail if no upstream)
    ahead = behind = 0
    rc2, rev_out, _ = await _git(
        cwd,
        "rev-list",
        "--left-right",
        "--count",
        "@{u}...HEAD",
    )
    if rc2 == 0:
        parts = rev_out.strip().split()
        if len(parts) == 2:
            behind, ahead = int(parts[0]), int(parts[1])

    # Changed files – normal mode collapses untracked directories into one
    # entry each (avoids thousands of rows on fresh repos).
    # -c core.quotepath=false prevents git from escaping non-ASCII filenames
    # (e.g. Chinese characters) into octal sequences.
    _, status_out, _ = await _git(
        cwd,
        "-c",
        "core.quotepath=false",
        "status",
        "--porcelain",
        "--untracked-files=normal",
    )
    changes: list[ChangedFile] = []
    for line in status_out.splitlines():
        if len(line) < 4:
            continue
        xy = line[:2]
        path = line[3:].strip()
        if " -> " in path:
            path = path.split(" -> ")[-1]
        # Strip trailing slash (git uses "dirname/" for untracked directories)
        path = path.rstrip("/")
        staged_char, unstaged_char = xy[0], xy[1]
        if staged_char not in (" ", "?"):
            changes.append(
                ChangedFile(path=path, status=staged_char, staged=True),
            )
        if unstaged_char == "?":
            changes.append(ChangedFile(path=path, status="?", staged=False))
        elif unstaged_char != " ":
            changes.append(
                ChangedFile(path=path, status=unstaged_char, staged=False),
            )

    return GitStatus(
        branch=branch,
        changes=changes,
        ahead=ahead,
        behind=behind,
    )


@router.get(
    "/branches",
    response_model=list[BranchInfo],
    summary="List branches",
)
async def list_branches(request: Request) -> list[BranchInfo]:
    workspace = await get_agent_for_request(request)
    rc, out, err = await _git(
        get_coding_dir(workspace),
        "branch",
        "-a",
        "--format=%(refname:short)|%(HEAD)",
    )
    _raise_if_not_repo(rc, err)
    branches: list[BranchInfo] = []
    for line in out.splitlines():
        line = line.strip()
        if not line:
            continue
        parts = line.split("|")
        name = parts[0].strip()
        current = len(parts) > 1 and parts[1].strip() == "*"
        remote = name.startswith("origin/") or name.startswith("remotes/")
        branches.append(BranchInfo(name=name, current=current, remote=remote))
    return branches


@router.post("/checkout", summary="Switch (or create) branch")
async def checkout_branch(body: CheckoutRequest, request: Request) -> dict:
    workspace = await get_agent_for_request(request)
    args = ["checkout"]
    if body.create:
        args += ["-b", body.branch]
    else:
        args.append(body.branch)
    rc, _, err = await _git(get_coding_dir(workspace), *args)
    if rc != 0:
        raise HTTPException(status_code=400, detail=err.strip())
    return {"branch": body.branch}


@router.get(
    "/diff",
    summary="Get diff for a file (or full staged/unstaged diff)",
)
async def get_diff(
    request: Request,
    path: str | None = None,
    staged: bool = False,
    untracked: bool = False,
) -> dict:
    """Return unified diff text."""
    workspace = await get_agent_for_request(request)
    cwd = get_coding_dir(workspace)

    if path and untracked:
        safe_join(cwd, path)
        rc, out, _ = await _git(
            cwd,
            "diff",
            "--no-index",
            "--",
            "/dev/null",
            path,
        )
        # --no-index returns exit code 1 when there is a diff
        if rc not in (0, 1):
            return {"diff": ""}
        return {"diff": out}

    args = ["diff"]
    if staged:
        args.append("--staged")
    if path:
        safe_join(cwd, path)
        args += ["--", path]
    rc, out, err = await _git(cwd, *args)
    if rc != 0:
        raise HTTPException(status_code=400, detail=err.strip())
    return {"diff": out}


