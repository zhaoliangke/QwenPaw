# Git command helper
# ---------------------------------------------------------------------------


async def _git(cwd: Path, *args: str) -> tuple[int, str, str]:
    """Run ``git <args>`` in *cwd*.

    Returns ``(returncode, stdout, stderr)``.
    """
    result = await run_command_async(
        ["git", *args],
        cwd=str(cwd),
        encoding="utf-8",
        errors="replace",
        check=False,
        timeout=None,
    )
    return result.returncode, result.stdout, result.stderr


def _raise_if_not_repo(rc: int, stderr: str) -> None:
    if rc != 0 and "not a git repository" in stderr.lower():
        raise HTTPException(status_code=422, detail="Not a git repository")


