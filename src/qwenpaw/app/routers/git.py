# Auto-init helper
# ---------------------------------------------------------------------------


async def _write_gitignore(cwd: Path) -> None:
    """Write a default .gitignore if one does not already exist."""
    gitignore_path = cwd / ".gitignore"
    if await asyncio.to_thread(gitignore_path.exists):
        return
    try:
        await asyncio.to_thread(
            gitignore_path.write_text,
            _DEFAULT_GITIGNORE,
            encoding="utf-8",
        )
    except Exception:
        pass


async def _exclude_nested_repos(cwd: Path) -> None:
    """Write nested git repos into .git/info/exclude so they are not staged."""

    def _scan() -> list[str]:
        try:
            return [
                p.parent.relative_to(cwd).as_posix() + "/"
                for p in cwd.rglob(".git")
                if p.is_dir() and p != cwd / ".git"
            ]
        except Exception:
            return []

    nested = await asyncio.to_thread(_scan)
    if not nested:
        return

    exclude_path = cwd / ".git" / "info" / "exclude"

    def _write_exclude() -> None:
        exclude_path.parent.mkdir(parents=True, exist_ok=True)
        existing = exclude_path.read_text() if exclude_path.exists() else ""
        with exclude_path.open("a") as fh:
            for rel in nested:
                if rel not in existing:
                    fh.write(rel + "\n")

    try:
        await asyncio.to_thread(_write_exclude)
    except Exception:
        pass


async def _auto_init_repo(cwd: Path) -> str:
    """Initialise a git repo in *cwd* if it is not already one.

    Returns the branch name after initialisation, or ``""`` on failure.
    """
    rc, _, err = await _git(cwd, "init")
    if rc != 0:
        logger.warning("git init failed in %s: %s", cwd, err)
        return ""

    await _write_gitignore(cwd)
    await _exclude_nested_repos(cwd)

    await _git(cwd, "add", "-A", "--ignore-errors")

    commit_rc, _, commit_err = await _git(
        cwd,
        "-c",
        "user.email=qwenpaw@localhost",
        "-c",
        "user.name=QwenPaw",
        "commit",
        "--allow-empty",
        "-m",
        "Initial commit",
    )
    if commit_rc != 0:
        logger.debug("Initial commit failed in %s: %s", cwd, commit_err)

    rc2, branch_out, _ = await _git(cwd, "rev-parse", "--abbrev-ref", "HEAD")
    return branch_out.strip() if rc2 == 0 else "main"


async def _resolve_branch(cwd: Path) -> tuple[str, bool]:
    """Return ``(branch, needs_init)``.

    ``needs_init`` is True when the workspace has no own git repo or its
    git top-level is a parent directory (inherited repo).
    """
    rc, toplevel, _ = await _git(cwd, "rev-parse", "--show-toplevel")
    if rc != 0:
        return "", True
    try:
        is_own = Path(toplevel.strip()).resolve() == cwd.resolve()
    except Exception:
        is_own = False
    if not is_own:
        return "", True
    rc2, branch_out, _ = await _git(cwd, "rev-parse", "--abbrev-ref", "HEAD")
    return branch_out.strip() if rc2 == 0 else "HEAD", False


# ---------------------------------------------------------------------------
# Models
# ---------------------------------------------------------------------------


class ChangedFile(BaseModel):
    path: str
    status: str  # "M", "A", "D", "?", "R", …
    staged: bool


class GitStatus(BaseModel):
    branch: str
    changes: list[ChangedFile]
    ahead: int = 0
    behind: int = 0


class BranchInfo(BaseModel):
    name: str
    current: bool
    remote: bool


class CommitInfo(BaseModel):
    hash: str
    author: str
    date: str
    message: str


class CheckoutRequest(BaseModel):
    branch: str
    create: bool = Field(
        default=False,
        description="Create the branch if it doesn't exist",
    )


class StageRequest(BaseModel):
    paths: list[str] = Field(
        default_factory=list,
        description="File paths to stage (empty = all)",
    )


class UnstageRequest(BaseModel):
    paths: list[str] = Field(default_factory=list)


class CommitRequest(BaseModel):
    message: str


class DiscardRequest(BaseModel):
    paths: list[str] = Field(
        default_factory=list,
        description="File paths to discard (empty = discard all)",
    )


class RevertRequest(BaseModel):
    commit_hash: str = Field(description="Commit hash to revert")


